# 4. 缺失数据
# 对于无法得到的数据点， R 允许向量包含特殊的 NA(Not Available) 值，
# 该值可以在计算中执行，即对 NA 的操作也产生 NA 作为结果
# e.g.
z <- c(1:3,NA) # z 是一个由数字和缺失数据构成的向量
ind <- is.na(z) # is.na(x) 定义一个与 z 长度相当的逻辑向量
# 当且仅当 z 中元素为缺失数据时，逻辑判断 TRUE
#
# 此外，还有一种计算中出现的缺失数据
# NAN: not a number
