# 向量/变量赋值后的进一步讨论
# 这里主要讨论向量运算、特殊符号、逻辑判断、缺失数据等
# 向量运算符号与普通运算符号一致，以下为性质：
# 如：
# 1. 向量与常数的加减乘除为向量的每一个元素与常数加减乘除；
# 2. 向量的乘方与开方为每个元素的乘方与开方；
# 3. 同样长度的向量的加减乘除等运算为对应元素进行加减乘除等；
# 4. 不同长度向量的加减乘除遵从循环法则（recycling rule）
#     注：通常要求向量的长度为倍数关系，否则会出现警告
#            “长向量并非短向量的整数倍”
#
